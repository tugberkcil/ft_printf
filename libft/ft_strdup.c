#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*s2;

	i = ft_strlen(s1);
	s2 = (char *)malloc(i + 1);
	if (!s2)
		return (0);
	ft_memcpy(s2, s1, i + 1);
	return (s2);
}
